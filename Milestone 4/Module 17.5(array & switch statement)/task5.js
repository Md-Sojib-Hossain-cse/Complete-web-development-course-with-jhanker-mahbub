//declaring two arrays of fruit
const desiFruits = ['mango','jackfruit','banana'];
const bideshiFruits = ['apple','orange','grapes'];

// Using the concat method to combine the two arrays into a new array
const fruits = desiFruits.concat(bideshiFruits);

// Printing both the original arrays and the combined array using console.log()
console.log(desiFruits);
console.log(bideshiFruits);
console.log(fruits);